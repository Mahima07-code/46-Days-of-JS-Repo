// -- Array --

let fruits = ['apple', 'banana', 'cherry'];
console.log(fruits[1]); // banana

// -- Object -- 

let person = { name: 'John', age: 25 };
console.log(person.name); // John

// -- Map -- 

let map = new Map();
map.set('name', 'Alice');
map.set(1, 'One');
console.log(map.get(1)); // One

// -- Sets --

let numbers = new Set([1, 2, 2, 3]);
console.log(numbers); // Set(3) {1, 2, 3}

